
function SomarNumeros(){
   let n1 = Number(document.getElementById("n1").value)
   let n2 = Number(document.getElementById("n2").value)
   alert(n1 + n2)
}

function SubtrairNumeros(){
   let n1 = Number(document.getElementById("n1").value)
   let n2 = Number(document.getElementById("n2").value)
   alert(n1 - n2)
}

function DividirNumeros(){
   let n1 = Number(document.getElementById("n1").value)
   let n2 = Number(document.getElementById("n2").value)
   alert(n1 / n2)
}

function MultiplicarNumeros(){
   let n1 = Number(document.getElementById("n1").value)
   let n2 = Number(document.getElementById("n2").value)
   alert(n1 * n2)
}
